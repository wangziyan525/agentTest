function countValues(obj){
    //不需要校验有效字段的个数
    var notNeedList=['AtchIndusDsc','LseMoRntl','PltMainVry','PltYrIncm','PltArea','BredVrty','BredYrIncm','BredQnty','OthrDsc'];

    return Object.keys(obj).filter(key =>{
        if(!notNeedList.includes(key)){
            return !!obj[key]
        }
    }).length
}