/**
 * 
 * @param {详情所有字段} param1 
 * @param {当前页面字段} param2 
 * @param {必输字段} keys 
 * @param {是否判断有效} flags 
 * @param {有效必输字段} param3 
 * @param {当前页面判断字段个数} needCounts  
 * @param {是不是农户} iffarmer  
 */
function filiterParam (param1, param2, keys,flags,param3,needCounts,iffarmer) {
    let newObjs = {};
    for(key1 in param1) {
        for (key2 in param2) {
            if (key2 == key1) {
                newObjs[key1] = param1[key1]
            }
        }
    };

    var newObj=Object.assign(param2,newObjs);//获取当前页面的字段并赋值
    var text="";
    //全部填写
    //部分填写
    //未填写
    var i=0;
    var Objectlength=Object.keys(newObj).length;

    for (key1 in newObj) {
        if (newObj[key1] != '') 
        ++i;
    }
   
    if(i==Objectlength){
        text= "全部已填写"
    }else if(i==0){
        text= "全部待填写"
    }else{

        //是否存在必填项
        if (keys && keys.length>0) {
            //必填项
            for (key1 in newObj) {
                for (key2 in keys) {
                    console.log(keys[key2])
                    if (key1 == keys[key2]) {//必填项未完成
                        if (newObj[key1] == '') {
                            text= "必填项未完成"
                            return text;
                        }
                    }
                }
            }

            //必填项已完成后判断是否需要判断有效

            //60%字段达标视为有效
            if(flags){
                
                text=checkIfEffect(newObj,param3,needCounts,iffarmer);
                return text;
            }
            text= "必填项已完成"
            return text;
        } else{
            //60%字段达标视为有效
           //60%字段达标视为有效
           if(flags){
                text=checkIfEffect(newObj,param3,needCounts,iffarmer);
                return text;
            }
            text= "部分已填写"
            return text;
        }
        
    }

    return text;


    
    
}

function checkIfEffect(newObj,param3,needCounts,iffarmer){
    
    //有效必输字段
    var needEffect=0;//有效校验必输字段
    if(param3 && param3.length>0){
        for(var k=0;k<param3.length;k++){
            if(!!!newObj[param3[k]]){
                console.log(param3[k])
                console.log(newObj[param3[k]])
                needEffect++; 
            }
            
        }

    }
    needCounts=Number(needCounts)
    var haseffective=Math.ceil(needCounts*0.6);
    var effective=countValues(newObj);

    var counts=haseffective - effective;

    if(needEffect>0 ||  counts>0){
        var returnNum=needEffect>counts?needEffect:counts;
        if(iffarmer=="farmer"){
            text= "还差"+returnNum+"个字段即可达成有效建档";
        }else{
            text= "还差"+returnNum+"个字段即可进行有效建档申报";
        }
        
        return text;


    }else{
        if(iffarmer=="farmer"){
            text= "已达成有效建档";
        }else{
            text= "可进行有效建档申报"
        }
        
        return text;
    }
}
